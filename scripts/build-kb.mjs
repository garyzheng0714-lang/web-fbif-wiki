async function createTable(name, defaultFieldName) {
  console.log(`📋 Creating table: ${name}...`);
  const data = await api("POST", `/bitable/v1/apps/${APP_TOKEN}/tables`, {
    table: { name },
  });
  const tableId = data.table_id;
  console.log(`   ✅ Created: ${tableId}`);

  // Rename the default first field
  const fieldsData = await api("GET", `/bitable/v1/apps/${APP_TOKEN}/tables/${tableId}/fields`);
  if (fieldsData.items && fieldsData.items.length > 0) {
    const firstField = fieldsData.items[0];
    await api("PUT", `/bitable/v1/apps/${APP_TOKEN}/tables/${tableId}/fields/${firstField.field_id}`, {
      field_name: defaultFieldName,
      type: 1,
    });
  }

  return tableId;
}

async function addFields(tableId, fields) {
  for (const field of fields) {
    try {
      await api("POST", `/bitable/v1/apps/${APP_TOKEN}/tables/${tableId}/fields`, field);
      await sleep(100); // Rate limiting
    } catch (e) {
      console.error(`   ⚠️ Field "${field.field_name}" error: ${e.message}`);
    }
  }
  console.log(`   ✅ ${fields.length} fields added`);
}

async function batchCreateRecords(tableId, records) {
  // Feishu batch API has limit of 500 per batch
  const batchSize = 100;
  let created = 0;
  for (let i = 0; i < records.length; i += batchSize) {
    const batch = records.slice(i, i + batchSize);
    await api("POST", `/bitable/v1/apps/${APP_TOKEN}/tables/${tableId}/records/batch_create`, {
      records: batch,
    });
    created += batch.length;
    console.log(`   ✅ ${created}/${records.length} records created`);
    await sleep(200);
  }
}

